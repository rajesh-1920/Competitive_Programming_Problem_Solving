void solve(void)
{
    int n;
    cin >> n;
    vector<int> v(n);
    for (auto &it : v)
        cin >> it;
    vector<int> pre(n + 1, 0);
    for (int i = 0; i < n; i++)
        pre[i + 1] = (n + (pre[i] + v[i]) % n) % n;
    map<int, int> mp;
    int ans = 0;
    for (auto it : pre)
        ans += mp[it], mp[it]++;
    cout << ans << '\n';
}
