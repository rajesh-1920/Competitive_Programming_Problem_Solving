void solve(void)
{
    int n, k, m;
    cin >> n >> k >> m;
    vector<int> v(n, -1);
    int c = 1;
    for (int i = n - k; i < n; i++)
        v[i] = c++;
    for (int i = n - k - 1; i >= 0; i--)
        v[i] = c++;
    for (auto &it : v)
        cout << it << ' ';
    cout << '\n';
}
