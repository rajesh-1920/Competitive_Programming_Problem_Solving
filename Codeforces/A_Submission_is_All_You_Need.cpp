//------------------------------(solve)----------------------------------------------------
void solve(void)
{
    int n;
    cin >> n;
    int ans = 0, c0 = 0, c1 = 0;
    while (n--)
    {
        int x;
        cin >> x;
        if (x == 0)
            c0++;
        else if (x == 1)
            c1++;
        else
            ans += x;
    }

    int mn = min(c0, c1);
    ans += (2 * mn);
    c1 -= mn;
    c0 -= mn;
    ans += c1 + c0;
    cout << ans << '\n';
}
